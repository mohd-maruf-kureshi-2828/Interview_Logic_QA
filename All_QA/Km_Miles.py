# 🔥 KM TO MILES CONVERSION

# 👉 1 kilometer = 0.621371 miles

# user se input lo
km = float(input("Enter distance in kilometers: "))

# conversion formula
# miles = km * 0.621371
miles = km * 0.62

# output print karo
print(f"{km} kilometers is equal to {miles} miles.")

